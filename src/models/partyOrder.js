class PartyOrder {
    constructor(id, name, phone, email, date, time, meridian, category, menu, status) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.email = email;
        this.date = date;
        this.time = time;
        this.meridian = meridian;
        this.category = category;
        this.menu = menu;
        this.status = status;
    }
}
export default PartyOrder;